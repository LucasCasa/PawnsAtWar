package ar.edu.itba.paw.webapp.DTOs;

public class UnitDTO {

  private int type;
  private int amount;

  public UnitDTO() {}

  public int getType() {
    return type;
  }

  public void setType(int type) {
    this.type = type;
  }

  public int getAmount() {
    return amount;
  }

  public void setAmount(int amount) {
    this.amount = amount;
  }
}
